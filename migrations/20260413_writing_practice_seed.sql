CREATE TABLE writing_practice_seed (
    writing_practice_seed_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    writing_level            writing_level_enum NOT NULL,
    writing_practice_type    writing_practice_type_enum NOT NULL,
    seq                      INT NOT NULL,
    prompt                   TEXT NOT NULL,
    answer                   TEXT NOT NULL,
    hint                     TEXT,
    CONSTRAINT uniq_writing_practice_seed_level_type_seq
        UNIQUE (writing_level, writing_practice_type, seq)
);

CREATE INDEX idx_writing_practice_seed_level_type
    ON writing_practice_seed (writing_level, writing_practice_type, seq);

