-- =============================================================================
-- 한글 자판 연습 — 자유 연습 시드 콘텐츠 (writing_practice_seed)
-- =============================================================================
-- study_task 수강 흐름과 독립된 자유 연습용 드릴 컨텐츠.
-- 레벨/유형별로 seq 순서대로 제공된다.
